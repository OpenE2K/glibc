double
__pow(double x, double y) {
	double yH, yL, zH, zL, p, q, r, s, t, u, v, w, z, nx, wx, X, Xi, Y, Yj;
	double r2, rH, rL, wH, wL, Ai, B, Bj, C, Ck, oddn;
	int i, j, k, jj/*, kk #70050 */, ii, yisint, n, m, hx, hy, ix, iy;
	unsigned lx, ly, kk;

	hx = HI(x);
	lx = LO(x);
	ix = hx & ~0x80000000;
	hy = HI(y);
	ly = LO(y);
	iy = hy & ~0x80000000;

	/* compute log2(x) using Quick Alogrithm */
	if ((hx | lx) == 0) {	/* special case for x=+0 */
		if ((iy | ly) == 0)
			return __kernel_standard (x, y, 20);
		i = iy | ((ly | -ly) >> 31);
		if (i > 0x7ff00000)
			return (y * y);	/* y is NaN; + -> * for Cheetah */
		else if (hy < 0) {
		        return (i == 0x7ff00000) ? (-y / x) :	/* y negative, return inf */
			        __kernel_standard (x, y, 43);	/* pow(+0.0,negative) */
		}
		else
			return (x);
	}
	j = 0x3fe6a09e;
	n = (ix - j) >> 20;
	i = ix - (n << 20);
	/* X = x normalized to [0x3fe6a09e,0x3ff6a09d] */
	HI(X) = i;
	LO(X) = lx;
	ii = ((i + 0x800) >> 12);
	HI(Xi) = ii << 12;
	LO(Xi) = 0;		/* Xi = X rounded to 8 bits */
	v = X - Xi;
	ii = ii - (j >> 12);
	ii = ii + ii + ii;	/* index */
	v *= _TBL_log2_X[ii + 2];	/* v = (X-Xi)/Xi */

	/*
	 * a slightly effective way to compute w =
	 * v*(C0+v*(C1+v*(C2+v*(C3+v*(C4+v*C5)))));
	 */
	r = v * v;
	s = C2 + r * C4;
	t = C3 + r * C5;
	j = 0x3ff00000;

	/* special case: y is +-inf, nan, +-1.0, or |y|<2**-64 */
	if ((((iy + 0x00100000) >> 20) - 0x3c0) < 0 || ((iy - j) | ly) == 0) {
		k = 0x7ff00000;
		if ((iy | ly) == 0) {
		        if (x != x) /* pow(NaN,0.0) */
		                return __kernel_standard (x, y, 42);
		        if ((ix | lx) == 0) /* pow(0.0,0.0) */
			        return __kernel_standard (x, y, 20);
			return (ONE);	/* x**0 = 1 */
		}
		else if (((hx - j) | lx) == 0)
			return (ONE);	/* C99: 1**y = 1 */
		else if (iy == j)	/* x**+-1 = x or 1/x */
			return (hy >= 0 ? ONE * x : (x != 0) ?
				( w = ONE / x, (fabs (w) != INFINITY) ? w :
				  __kernel_standard (x, y, 21) ) : /* pow overflow */
				  __kernel_standard (x, y, 23)); /* pow(-0.0,negative) */
		else if (k - (int) (ix | ((lx | -lx) >> 31)) < 0 ||
			k - (int) (iy | ((ly | -ly) >> 31)) < 0)
			return (x * y);	/* x or y is NaN; + -> * for Cheetah */
					/* includes Sun: 1**NaN = NaN */
		else if (iy == k) {	/* y is +-inf */
			if (((ix - j) | lx) == 0) {
				return (ONE);
						/* C99: -1**+-inf = 1 */
			} else if (hy > 0)
				/* +-(|x|>1)**+inf=+inf, +-(|x|<1)**+inf=+0 */
				return (ix >= j ? y : ZERO);
			else	/* +-(|x|>1)**-inf=+0, +-(|x|<1)**-inf=+inf */
				return (ix >= j ? ZERO : -y);
		} else if ((ix | lx) == 0) {
//		        if (hx < 0 && hy < 0)
//		                return __kernel_standard (x, y, 43); /* pow(+0.0,negative) ????? */
//			/* +-0**(1>y>0) = 0, +-0**(-1<y<0) = inf */
//			return (hy >= 0 ? ZERO : ONE / ZERO);
		}
		else if (ix == k)	/* +-inf**+-(1>y>0) = +inf,+0 */
			return (hy >= 0 ? x * x : ZERO);
		else if (hx < 0)
			/* (-x except 0,inf)**(non-integer) is NaN w/signal */
			return __kernel_standard (x, y, 24);
		else {
			/*
			 * The remaining case is a positive normal x ** tiny y.
			 * By actual testing, we find that x**y rounded to 1 as
			 * long as |y|<=2**-64; thus,
			 * x**y = exp(y*lnx) ~= 1 + y*lnx ~= 1 + sign(x-1)*y
			 */
			HI(y) = hy ^ (((ix - j) >> 31) << 31);
			return (ONE + y);
		}
	}
	s = C0 + r * s;
	t = C1 + r * t;
	oddn = ONE;
	yisint = 0;

	/* special cases: x is subnormal, 0, negative, +-inf, or nan */
	if (hx + 0x00100000 < 0x00200000) {
		i = iy >> 20;
		if (0x7ff00000 - (int) (ix | ((lx | -lx) >> 31)) < 0)
			return (x * x);	/* x is NaN; + -> * for Cheetah */
		if (hx < 0) {
			/*
			 * determine if y is an odd int when x < 0
			 * yisint = 0 ... y is not an integer
			 * yisint = 1 ... y is an odd int
			 * yisint = 2 ... y is an even int
			 */
			if (i >= 0x434)
				yisint = 2;	/* even integer y */
			else if ((k = i - 0x3ff) >= 0) {
				if (k > 20) {
					j = ly >> (52 - k);
					if ((j << (52 - k)) == ly)
						yisint = 2 - (j & 1);
				} else if (ly == 0) {
					j = iy >> (20 - k);
					if ((j << (20 - k)) == iy)
						yisint = 2 - (j & 1);
				}
			}
			HI(oddn) = 0x3ff00000 | (yisint << 31);
						/* oddn= -1.0 if yisint==1 */
			HI(x) = ix;
		}
		if ((ix | lx) == 0)		/* +-0**+-y = s*0 or s*inf */
			return (hy >= 0 ? oddn * ZERO :
				__kernel_standard (x, y, (yisint == 1) ? 23 : 43)); /* pow(+-0.0,negative) */
		else if (ix == 0x7ff00000)	/* +-inf**+-y = s*inf or s*0 */
			return (hy >= 0 ? x * oddn : ZERO * oddn);
		else if ((((hx >> 31) + 1) | yisint) == 0)
			return __kernel_standard (x, y, 24);	/* neg**non-int = NaN */
		else if (ix < 0x00100000) {	/* x subnormal */
			if (((hy - 0x3fe00000) | (hx >> 31) | ly) == 0)
				return (__builtin_sqrt(x));	/* y is 0.5, x>=0 */
			x *= TWO54;
			hx = HI(x);
			lx = LO(x);
			ix = hx & ~0x80000000;
			/* recompute the Quick algorithm */
			j = 0x3fe6a09e;
			n = (ix - j) >> 20;
			i = ix - (n << 20);
			/* X = x normalized to [0x3fe6a09e,0x3ff6a09d] */
			HI(X) = i;
			LO(X) = lx;
			ii = ((i + 0x800) >> 12);
			HI(Xi) = ii << 12;
			LO(Xi) = 0;	/* Xi = X rounded to 8 bits */
			v = X - Xi;
			ii = ii - (j >> 12);
			ii = ii + ii + ii;	/* index */
			v *= _TBL_log2_X[ii + 2];	/* v = (X-Xi)/Xi */
			r = v * v;
			s = (C0 + r * (C2 + r * C4));
			t = (C1 + r * (C3 + r * C5));
			n -= 54;
		}
	}
	s = v * s;
	t = r * t;
	if (((hy - 0x40000000) | ly) == 0) {
                w = x * x;
		if (fabs (w) == INFINITY)
		        return __kernel_standard (x, y, 21); /* pow overflow */
		else if (w == 0)
		        return __kernel_standard (x, y, 22); /* pow underflow */
		return w;		/* y is 2 */
	}
	w = s + t;
	if (((hy - 0x3fe00000) | (hx >> 31) | ly) == 0)
		return (__builtin_sqrt(x));	/* y is 0.5, x>=0 */

	nx = (double) n;
	if (n != 0) {
		nx += _TBL_log2_X[ii];
		wx = w + _TBL_log2_X[ii + 1];
		i = (n >> 31);
		z = nx + wx;
		if (((64 - ((n ^ i) - i)) | (iy - 0x40400000)) < 0) {
							/* |n|>64 or y<32 */
			if (iy > 0x40400000) {
				/* if y>32, then |n|>64; hence over/underflow */
				if ((n ^ hy) >= 0)
					return __kernel_standard (oddn * x, y, 21); /* pow overflow */
				else
					return __kernel_standard (oddn * x, y, 22); /* pow underflow */
			}
		} else {
			Y = v + ONE;	/* Y = X/Xi */
			jj = (HI(Y) + 0x20) >> 6;
			HI(Yj) = jj << 6;
			LO(Yj) = 0;		/* Yj = Y rounded to 14 bits */
			u = X - Xi * Yj;
			jj -= 0xffbfc0;		/* (0x3feff000)>>6) */
			jj = jj + jj + jj;	/* index */
			u *= _TBL_log2_Y[jj + 2] * _TBL_log2_X[ii + 2];
			nx += _TBL_log2_Y[jj];
			/*
			 * p = u*(G0+u*(G1+u*(G2+u*(G3+u*G4))));
			 */
			r = u * u;
			p = u * (G0 + r * (G2 + r * G4));
			q = r * (G1 + r * G3);
			p += q;
			wx = (_TBL_log2_X[ii + 1] + _TBL_log2_Y[jj + 1]);
			wx += p;
			if (iy > 0x40add400) {
				/* if |y|>2154, must over/underflow */
				if ((n ^ hy) >= 0)
					return __kernel_standard (oddn * x, y, 21); /* pow overflow */
				else
					return __kernel_standard (oddn * x, y, 22); /* pow underflow */
			}
			z = nx + wx;
		}
		z *= y;
		i = ((((unsigned) ly) >> 26) + 1) >> 1;
		z *= C256;
		HI(yH) = hy + (i >> 5);
		t = y * wx;
		LO(yH) = i << 27;
		yL = y - yH;
		zH = yH * nx;
		z += HALF[((unsigned) (n ^ hy)) >> 31];
		zL = yL * nx + t;
		m = (int) z;
		r = zL - ((double) m * INV256 - zH);
		k = m >> 8;
		p = D2 + r * (D3 + r * D4);
		LO(s) = 0;
		j = (m & 0xff) << 1;
		HI(s) = (0x3ff00000 + (k << 20)) | (yisint << 31);
		p = r * (D1 + r * p);
		q = _TBL_exp2_z[j];
		t = _TBL_exp2_z[j + 1] + q * p;
		w = t + q;
		if (((1023 - k) | (k + 1021)) < 0) {	/* k>1023 or k<-1021 */
			i = HI(w);
			j = (i >> 20) + k;
			if (j <= 0) {	/* subnormal or underflow */
				if (j <= -54) {
					return __kernel_standard (oddn * x, y, 22); /* pow underflow */
				} else {
					k += 100;
					HI(w) = i + (k << 20);
					s = TWOM100 * oddn;
				}
			} else if (j >= 0x7ff) {
				return __kernel_standard (oddn * x, y, 21); /* pow overflow */
			} else {
				HI(w) = i + (k << 20);
				s = oddn;
			}
		}
		return (s * w);
	} else {		/* if n is 0 */
		i = (ix + 0x2000) & 0xffffc000;
		if (i == 0x3ff00000) {	/* -2**-8 <= x-1 < 2**-7 */
			r = X - ONE;
			k = 0x3ff00000;
			i = ix - k;
			r2 = r * r;
			j = ((ix + 0x40) & 0xffffff80);
			if (j == k) {	/* -2**-15<=r<=2**-14 */
				if ((i | lx) == 0)	/* x = +-1.0 */
					return (x * oddn);
				wL = r2 * (HALFN + r * (T1 + r * (T2 + r * T3)));
				if (iy > 0x43f00000) {
					/* if |y|>2**64, must over/underflow */
					if (((ix - 0x3ff00000) ^ hy) >= 0)
				                return __kernel_standard (oddn * x, y, 21); /* pow overflow */
					else
					        return __kernel_standard (oddn * x, y, 22); /* pow underflow */
				}
				w = r + wL;
				i = ((((unsigned) LO(w)) >> 26) + 1) >> 1;
				HI(wH) = HI(w) + (i >> 5);
				LO(wH) = i << 27;
				wL += (r - wH);
				j = ((((unsigned) ly) >> 26) + 1) >> 1;
				HI(yH) = hy + (j >> 5);
				LO(yH) = j << 27;
				yL = y - yH;
			} else {	/* -2**-8<=x-1<2**-7 */
				p = P0 + r2 * (P2 + r2 * P4);
				q = r * (P1 + r2 * (P3 + r2 * P5));
				i = ((((unsigned) LO(r)) >> 26) + 1) >> 1;
				HI(rH) = HI(r) + (i >> 5);
				LO(rH) = i << 27;
				rL = r - rH;
				t = r * (p + q);
				w = r + r2 * (t - HALFP);
				if (iy >= 0x41800000) {
					/* if |y|>=2**26 overflow/underflow */
					if (((ix - 0x3ff00000) ^ hy) >= 0)
				                return __kernel_standard (oddn * x, y, 21); /* pow overflow */
					else
					        return __kernel_standard (oddn * x, y, 22); /* pow underflow */
				}
				i = ((((unsigned) LO(w)) >> 26) + 1) >> 1;
				HI(wH) = HI(w) + (i >> 5);
				LO(wH) = i << 27;
				wL = (((r - wH) - (HALFP * rH * rH)) + r2 * t) -
					rL * (rH + HALFP * rL);
				j = ((((unsigned) ly) >> 26) + 1) >> 1;
				HI(yH) = hy + (j >> 5);
				LO(yH) = j << 27;
				yL = y - yH;
			}
		} else {
			ii = (ix + 0x1000) >> 13;
			HI(Ai) = ii << 13;
			LO(Ai) = 0;	/* Ai = x rounded to 7 bits */
			ii -= 0x1ff35;
			ii = ii + ii + ii;
			B = x * _TBL_log_A[ii + 2];
			wH = _TBL_log_A[ii];
			wL = _TBL_log_A[ii + 1];
			if (iy > 0x41100000) {
				/* if |y|>2**18, must over/underflow */
				if (((ix - 0x3ff00000) ^ hy) >= 0)
				        return __kernel_standard (oddn * x, y, 21); /* pow overflow */
				else
					return __kernel_standard (oddn * x, y, 22); /* pow underflow */
			}
			jj = (HI(B) + 0x20) >> 6;	/* rounded to 14 bits */
			HI(Bj) = jj << 6;
			LO(Bj) = 0;
			jj -= 0xffbf80; /* 0x3fefe000>>6 */
			jj = jj + jj + jj;
			C = B * _TBL_log_B[jj + 2];
			wH += _TBL_log_B[jj];
			wL += _TBL_log_B[jj + 1];
			i = (((unsigned) LO(C)) >> 30) + 1;
			kk = HI(C) + (i >> 2);	/* add carry */
			k = (i & 2) << 30;
			HI(Ck) = kk;
			LO(Ck) = k;
			kk = ((kk - 0x3fefffc0) << 1) + (((unsigned) k) >> 31);
			kk = kk + kk + kk;
			v = (x - Ai * Bj * Ck) * _TBL_log_A[ii + 2] *
				_TBL_log_B[jj + 2] * _TBL_log_C[kk + 2];
			wH += _TBL_log_C[kk];
			wL += _TBL_log_C[kk + 1];
			wL += v * (ONE + v * (v * Q3 - HALFP));
			j = ((((unsigned) ly) >> 26) + 1) >> 1;
			HI(yH) = hy + (j >> 5);
			LO(yH) = j << 27;
			yL = y - yH;
		}
		zL = yL * wH + y * wL;
		zH = yH * wH;
		z = zH + zL;
		m = (int) (z * INVLN2_256 +
			HALF[((unsigned) ((ix - 0x3ff00000) ^ hy)) >> 31]);
		r = zL - m * LN2_256L;
		r += zH - m * LN2_256H;
		p = r * (ONE + r * (B1 + r * (B2 + r * B3)));
		k = m >> 8;
		j = (m & 0xff) << 1;
		HI(s) = (0x3ff00000 + (k << 20)) | (yisint << 31);
		LO(s) = 0;
		q = _TBL_exp2_z[j];
		t = _TBL_exp2_z[j + 1] + q * p;
		t += q;
		if (((1023 - k) | (k + 1021)) < 0) {	/* k>1023 or k<-1021 */
			i = HI(t);
			j = (i >> 20) + k;
			if (j <= 0) {	/* subnormal or underflow */
				if (j <= -54) {
					return __kernel_standard (oddn * x, y, 22); /* pow underflow */
				} else {
					k += 100;
					HI(t) += k << 20;
					s = oddn * TWOM100;
				}
			} else if (j >= 0x7ff) {
				return __kernel_standard (oddn * x, y, 21); /* pow overflow */
			} else {
				HI(t) += k << 20;
				s = oddn;
			}
		}
		return (s * t);
	}
}

