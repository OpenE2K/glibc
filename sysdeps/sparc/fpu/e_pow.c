/*
 * fast pow return x**y
 *
 * Method:
 *
 *      x**y = exp(y*log(x)) = 2**(y*log2(x))
 *
 * Quick algorithm
 * ---------------
 *   Let
 *	x = 2**n * (1+f), where	sqrt(0.5)<f<sqrt(2), or  1/sqrt2-1<f<sqrt2-1
 *   then
 *	log2(x) = n + log2(1+f)
 *   Let
 *	X=1+f 			... range of X is [0x3fe6a09e - 0x3ff6a09e]
 *   Let Xi = X rounded to 8 bits
 *	Xi=(X+0x800)&0xfffff000 ... range of Xi is [0x3fe6a000 -- 0x3ff6a000]
 *   where
 *	i = (Xi-0x3fe6a000)>>12 ... range of i is 0,...0x100 (0,...,256)
 *   then
 * (1)	log2(x) = n + log2(Xi) + log2(X/Xi)
 *
 *   To compute (1), we break log2(Xi) into log2_XH+log2_XL, where
 *	log2_XH =(log2(Xi) + s*2**36)-s*2**36	... 16 bits after binary point
 *	log2_XL = log2(Xi) - log2_XH		... always less than 2**-16
 *   where s = sign(log2(Xi)).
 *   Note that Xi = 0x3fe6a000 + (i<<12)
 *   For log2(X/Xi), let v = (X-Xi)*(1/Xi), then  |v|<=2**-9 and
 *	log2(X/Xi) = log2(1+v) = w = v*(C0+v*(C1+...C5)...) where the polynomial
 *   approximation has relative error less than 2**-62.26 in absolute value.
 *
 *   A picture below show the lenght and size of the crucial elements
 *
 *		n:	[--].			... 11 bits for integer n
 *		log2_XH	    [-----]		... 16 bits (after binary pt.)
 *		log2_XL		  [-----...]  	... less than 2**-16
 *		w=log2(X/Xi)	[------...]   	... less than 2**-8
 *
 *   Thus, let 	nx = n+log2_XH, wx = log2_XL+w,  then nx has at most 27
 *   significant bits, |wx|<= 2**-8, and
 * (2)	log2(x) = nx + wx
 *
 *   The next step is to compute
 * (3)	y*log2(X) = k + (j/256) + r 	... j,k integer, |r|<=1/512.
 *   First break y = yH+yL, where yH = y rounded to 26 bits. Then
 *   compute z = zH+zL = y*log2(x) by
 *	zH= yH*nx,
 *	zL= yL*nx+y*wx
 *	z = zH+zL.
 *   To determine j,k, let m= z*256+0.5*sign(z) (z*256 rounded to an integer),
 *   then
 *	k=m>>8
 *	j=m&0xff
 *   and the remainder
 *	r = (zH-m*(1/256.0))+zL
 *
 *   From (3) we have
 * (4)	x**y = 2**k * 2**(j/256) * 2**(r),
 *   The computation of 2**(j/256) is by table lookup
 *	2**(j/256) = exp2_zH[j]+exp2_zL[j]
 *   where
 *	exp2_zH[j] = 2**(j/256) rounded, and
 *	exp2_zL[j] = 2**(j/256)-exp2_zH[j]
 *   The computation of 2**r is by 1 + (2**r - 1), where
 *	2**r - 1 = p = r*(D1+r*(D2+r*(D3+r*D4)))	(accuracy 2**-60)
 *
 *   Finally (4) is computed by
 *	2**k * (exp2_zH[j] + (exp2_zL[j]+exp2_zH[j]*p))
 *
 *   Note 1:
 *      This scheme guarantees less than 1ulp accuracy if (j/256+r) have 3 extra
 * 	bits accuracy. Since we are using an 8-bit table lookup Xi, log2(x)
 *	has about log2(n)+8 extra bits (assume n!=0). Therefore, k can go up
 *	to log2(n)+5 bits, or k < 32n. Since y*log2(X)~y*n, we have |y|<32.
 *	In other word, as long as |y|<32 and n!=0, one can use the Quick
 *	algorithm safely. Also, when n>=2**6, it is also safe to use the
 *	Quick algorithm, for if |y|>=32, y*n >= 2**11 and the result will
 *	over/underflow. Thus the Quick algorithm applies to
 *		x in  [0,0.707] U [1.42,oo) and y in [0,32).
 *	This range will cover a majority of data in current spec program,
 *	including DODUC.
 *
 *   Note 2:
 *	When |n|<2**6 and |y|>=32, 6 more bits is needed in log2(X).
 *	(A quick way to compute |n|: i=(n>>31), |n|=(n^i)-i.)
 *	That is,
 *	log2(X) will need 14 extra bit so that when k reaches it maximum 11
 *	bit, we still have 3 bits left for the r. Here is a way to add in 6
 *	more bits. Let Y  = X*(1/Xi). Since both X and Xi have the same
 *	exponent, we may assume that they are normalized. Xi is
 *	obtained by rounding X to 8 bits; thus we have X = Xi + eps,
 *	where -2**-9<=eps< 2**-9.  Thus X/Xi = (Xi+eps)/Xi
 *	= 1 + eps/Xi = 1 + eps'. Since Xi>=1, we have -2**-9 <= eps < 2**-9.
 *	Therefore, Y is between
 *		0.1111111110000000000... and 1.0000000001111111111...
 *	or Hi(Y) rounded is between
 *		0x3feff000, 0x3ff00800
 *	Let
 *		Yj = (Y+0x20)&0xffffffc0,	...rounded to 14 bits
 *		j  = (Yi-0x3feff000)>>6		...j=0,...,96
 *	Then for j=0,...,96
 *		Yj = 0x3feff000 + (i<<6) 	...[0x3feff000,0x3ff007c0]
 *	We have
 *		log2(X) = log2(Xi) + log2(Yi) + log2(X/Xi/Yj)
 *	Here
 *		log2(X/Xi/Yj) = log2(X/(Xi*Yj))
 *			      = log2(1+u)
 *			      = u*(G0+u*(G1+u*(G2+u*(G3+u*G4))))
 *	where u = (X-Xi*Yj)*(1/Xi)*(1/Yj), |u|<=2**-15. The approximation
 *	accuracy is 2**-80.
 *	For the value of log2(Yj), we break it into
 *		log2_YH[j] + log2_YL[j] where log2_YH[j] is 16 bits after binary
 *	That is (s= sign(log2(Yj)))
 *		log2_YH[j] = (log2(Yj)+s*2**36)-s*2**36
 *		log2_YL[j] = log2(Yj) - log2_YL[j]
 *
 *	Step (2) of the Quick algorithm is then modified to be
 *		nx = n + log2_XH[i]+log2_YH[j]
 *		wx = log2_XL[j]+log2_YL[j]+log2(1+u)
 *	The rest is the same as the Quick algorithm.
 *
 *   Note 3: n=0.
 *      Case 1: -2**-8 <= x-1 < 2**-7
 *	------
 *	If (Hi(x)+0x2000)&0x7fffc000 == 0x3ff00000, then we have
 *		-2**-8 <= x-1 < 2**-7
 *	In this case, we directly use the following method to compute
 *	log(x): let r = x-1,
 *		log(1+r) = r - 0.5*r*r + r*r*r*Poly(r),
 *	where
 *		Poly(r) = P0+r*(P1+...r*P5)
 *   	with precision = 2**(-69)
 *
 *	If one compute r-0.5*r*r carefully, then the third term of log(1+r)
 *	will have 14 bits shift right and thus guarantee 14 bits extra accuracy.
 *	Let
 *		rH = r rounded to 26 bits,
 *		rL = r-rH
 *		r2 = r*r
 *		t  = r*Poly(r)
 *		w  = r+r2*(t-0.5);
 *		wH = w rounded to 26 bits
 *		wL = (((r-wH)-(0.5*rH*rH))+r2*t)-rL*(rH+0.5*rL)
 *	then
 *		log(1+r) = wH + wL
 *
 *      Case 2 Otherwise
 *	------
 *	Use three 7 bits tables: let
 *		Ai = (X+0x1000)&0x7fffe000	... rounded to 7 bits
 *		i  = (Ai-0x3fe6a000)>>13 	... i = 0,...,128
 *		B  =  X*(1/Ai)			... Ai=0x3fe6a000+(i<<13)
 *		Bj = (B+0x20)&0x7fffffc0	... rounded to 14 bits
 *		j  = (Bj-0x3fefe000)>>6		... j = 0,...,192
 *		C  =  B*(1/Bj)			... Bj=0x3fefe000+(i<<6)
 *		Lo(Ck) = (Lo(Ck)+0x40000000)&0x80000000
 *		TEMP = (((unsigned)Lo(C))>>30)+1
 *		Hi(Ck) = Hi(C)+(TEMP>>2) 	... add carry
 *		Lo(Ck) = (TEMP<<30)&0x80000000
 *		k  = ((Ck-0x3fefffc0)<<1)+((Lo(Ck)>>31)&1)... k = 0,...,192
 *						... Ck=0x3fefffc0+(k>>1)
 *						... Lo(Ck)= (K&1)<<30
 *	then
 *		log(x)=log(Ai)+log(Bj)+log(Ck)+log(X/Ai/Bj/Ck)
 *	Here we choose the High part of the table value as follows
 *	so that the sum would be exact.
 *		log_AH    .[-----------]	... 26 bits (after binary pt.)
 *		log_BH    .   [--------]	... 19 bits
 *		log_CH    .      [-----]	... 12 bits
 *	That is,
 *	log_AH[i] = (log(Ai)+s*2**26)-s*2**26, log_AL[i]=log(Ai)-log_AiH[i]
 *	log_BH[j] = (log(Bj)+s*2**26)-s*2**26, log_BL[j]=log(Bj)-log_BjH[j]
 *	log_CH[k] = (log(Ck)+s*2**26)-s*2**26, log_CL[k]=log(Ck)-log_CkH[k]
 *	where s is the sign of log(#).
 *	Then
 *		v  = (X-Ai*Bj*Ck)*(1/Ai)*(1/Bj)*(1/Ck)	... |v|<=2**-22
 *		wH = log_AH[i]+log_BH[j]+log_CH[k]
 *		wL = (log_AL[i]+log_BL[j]+log_CL[k])+log(1+v)
 *	where
 *		log(1+v)= v*(1.0+v*(-0.5+v*Q3))
 *	with Precision   =  2**(-68.00)
 *	Then
 *		log(1+r) = wH + wL
 *
 *      Now we proceed to compute x**y by exp(y*log(x)) as follow:
 *
 *		y = yH+yL, where yH = y rounded to 26 bits
 *		zH = yH*wH
 *		zL = yL*wH+y*wL
 *		z  = zH+zL
 *		m  = z*(256/ln2)+0.5*sign(z)
 *		k  = m>>8
 *		i  = m&0xff
 *		ln2_256H = ln2/256 chopped to 32 bits
 *						...so that m*ln2_256H exact
 *		ln2_256L = ln2/256 - ln2_256H
 *		r = (zH-m*ln2_256H)+(zL-m*ln2_256L)
 *	Now z = k*ln2 + (i/256)*ln2 + r, with |r|<0.5*(ln2/256)=1.3538..e-3
 *	We have
 *		exp(z) = 2**k * (2**(1/256) * (1+p)
 *	where
 *		p = exp(r)-1 = r*(1.0+r*(B1+r*(B2+r*B3));
 *	with precision = 2**-57.45
 *	Finally, we compute exp(z) as follow
 *		2**k * (exp2_zH[j] + (exp2_zL[j]+exp2_zH[j]*p))
 *
 * Special cases:
 * --------------
 *	1.  (anything) ** 0  is 1
 *	1'. 1 ** (anything)  is 1	(C99; 1 ** +-INF/NAN used to be NAN)
 *	2.  (anything) ** 1  is itself
 *	3.  (anything) ** NAN is NAN
 *	4.  NAN ** (anything except 0) is NAN
 *	5.  +-(|x| > 1) **  +INF is +INF
 *	6.  +-(|x| > 1) **  -INF is +0
 *	7.  +-(|x| < 1) **  +INF is +0
 *	8.  +-(|x| < 1) **  -INF is +INF
 *	9.  -1          ** +-INF is 1	(C99; -1 ** +-INF used to be NAN)
 *	10. +0 ** (+anything except 0, NAN)               is +0
 *	11. -0 ** (+anything except 0, NAN, odd integer)  is +0
 *	12. +0 ** (-anything except 0, NAN)               is +INF
 *	13. -0 ** (-anything except 0, NAN, odd integer)  is +INF
 *	14. -0 ** (odd integer)  = -(+0 ** (odd integer)), hence
 *	    -0 ** (+odd integer) = -0
 *	    -0 ** (-odd integer) = -INF
 *	15. +INF ** (+anything except 0,NAN) is +INF
 *	16. +INF ** (-anything except 0,NAN) is +0
 *	17. -INF ** (anything)  = -0 ** (-anything), hence
 *	    -INF ** (+anything except 0,NaN)  = +INF
 *	    -INF ** (-anything except 0,NaN,odd integer)  = +0
 *	    -INF ** (+odd integer) = -INF
 *	    -INF ** (-odd integer) = -0
 *	18. (-anything) ** (integer) is ((-1)**(integer)) * (+anything**integer)
 *	19. (-anything except 0 and inf) ** (non-integer) is NAN
 *
 * Accuracy:
 *	pow(x,y) returns x**y nearly rounded. In particular
 *			pow(integer,integer)
 *	always returns the correct integer provided it is
 *	representable.
 */
