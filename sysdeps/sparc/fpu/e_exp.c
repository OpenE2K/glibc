double
__ieee754_exp(double x) {
	double r, p, s, w, t, *tb;
	int hx, m, k, j, i, lx;

	hx = HI(x);
	lx = LO(x);
	i = (hx >> 20) & 0x7ff;
	/* for arg bigger than 16 */
	if (i >= 0x403) {
		if (i >= 0x408) {	/* |x| >= 512.0 */
			/* |x| >= 709.00 */
			if ((unsigned) (hx << 1) >= (unsigned) 0x810C5000) {
				if (i >= 0x7ff) {	/* x is nan of +-inf */
					if (((hx - 0xfff00000) | lx) == 0)
						return (ZERO);
					return (x * x);	/* + -> * for Cheetah */
				}
				/* if x > 709.782712893383973096 overflow */
				if (x > OVFTH)
					 return __kernel_standard (x, x, 6);
				/* if x< -745.13321910194110842 underflow */
				if (x < UNFTH)
					 return __kernel_standard (x, x, 7);
				lx = 1 + ((hx >> 31) << 1);	/* lx=1 or -1 */
			} else
				lx = hx >> 31;	/* lx=0 or -1 */
		} else
			lx = 0;
		x -= (double) ((int) x);
		i = 1038 - i;
		k = hx >> 31;
		j = ((k & 0x200) | ((hx >> i) & 0x1f)) << 1;
		tb = (double *) _TBL_xexp + j;
		r = x - (*tb);	/* r = x-_TBL_xexp[j]; */
		t = *(tb + 1);	/* t = _TBL_xexp[j+1]; */
		s = (r + (r * r) * (C1 + C2 * r)) + ((r * r) * (r * r)) *
			((C3 + C4 * r) + C5 * (r * r));
		m = (((((hx >> 9) & 0x7ff) | 0x800) >> (i - 4)) + (k & 709)) <<
			1;
		tb = (double *) _TBL_expj + m;
		p = *(tb);
		w = *(tb + 1);
		s = t * w + ((p + w) * t) * s;
		p *= t;
		if (lx < 0)
			return (TWOM200 * (s + p));
		else if (lx > 0)
			return (TWOP200 * (s + p));
		else
			return (s + p);
	}
	/* tiny x */
	if (i < 0x3f9) {	/* |x|<2**-6 */
		if (((hx << 1) | lx) == 0)
			return (ONE);
		if (i < 0x3e4)
			return (ONE + x);
		s = (x + (x * x) * (C1 + C2 * x)) + ((x * x) * (x * x)) *
			((C3 + C4 * x) + C5 * (x * x));
		return (ONE + s);
	}
	/* for arg between 2**-6 and 16 */

	/* index */
	k = ((hx >> 12) | 0x100) & 0x1ff;
	j = (((hx >> 31) & 0x200) + (k >> (1026 - i))) << 1;
	tb = (double *) _TBL_xexp + j;
	r = x - (*tb);		/* r = x-_TBL_xexp[j]; */
	t = *(tb + 1);		/* t = _TBL_xexp[j+1]; */
	s = (r + (r * r) * (C1 + C2 * r)) + ((r * r) * (r * r)) *
		((C3 + C4 * r) + C5 * (r * r));
	return (t + t * s);
}

