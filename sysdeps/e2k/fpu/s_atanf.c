FL __atanf (FL X)
{
    _type_float_bits arg, arg1;
    DB y, yv2, yp;
    int ind;
#if __iset__ >= 6
    _type_float_bits ires;
    _type_double_bits ly, lyv2, lyv4, lyv5, lres, lres1, lres2;
#endif /* __iset__ < 6 */

    if (X == 0) return X; /* for X = -0.0 only */

    arg1.value = X;
    arg.value = ffabs (X);
    y = (DB) X;
    yv2 = y * y;

    if (arg.int0 <= 0x3e800000) { /* |x|<=0.25 */
#if __iset__ >= 6
        ly.value = y;
        lyv2.value = yv2;
        lyv4.value = yv2 * yv2;
        lyv5.value = lyv4.value * y;
        lres.value = KA3 * y;
        lres2.llong = __builtin_e2k_fmad (lres.llong, lyv2.llong, ly.llong);
        lres1.llong = __builtin_e2k_fmad (LKA9, lyv2.llong, LKA7);
        lres1.llong = __builtin_e2k_fmad (lres1.llong, lyv2.llong, LKA5);
        lres.llong = __builtin_e2k_fmad (lres1.llong, lyv5.llong, lres2.llong);
        return (FL) lres.value;
#else /* __iset__ < 6 */
        return (FL) (y + KA5 * y * yv2 * (KA3/KA5 + yv2) + yv2 * (yv2 * yv2) * (KA7 * y + KA9 * y * yv2));
#endif /* __iset__ < 6 */
    }
    if (arg.int0 <= 0x40800000) { /* |x|<=4 */
        ind = (arg.int0 - 0x3e800000) >> 17;
        arg.int0 = arg1.int0 & 0xfffe0000;
#if __iset__ >= 6
        ires.int0 = __builtin_e2k_fmas (arg1.int0, arg.int0, 0x3f800000);
        y = (DB) ((X - arg.value) / ires.value);
#else /* __iset__ < 6 */
        y = (DB) ((X - arg.value) / (1.0F + X * arg.value));
#endif /* __iset__ < 6 */
        yp = constnarctg[ind];
        yp = (arg1.int0 < 0) ? y - yp : y + yp;
#if __iset__ >= 6
        ly.value = yp;
        lres.value = y * y;
        lres1.value = KB3 * y;
        lres.llong = __builtin_e2k_fmad (lres1.llong, lres.llong, ly.llong);
        return (FL) lres.value;
#else /* __iset__ < 6 */
        return (FL) (yp + KB3 * y * (y * y));
#endif /* __iset__ < 6 */
    }
    if (arg.int0 <= 0x44800000) { /* |x|<=2^10 */
#if __iset__ >= 6
        lyv2.value = yv2;
        lres.llong = __builtin_e2k_fmad (LKP1, lyv2.llong, LKP3);
        lres1.value = yv2 + KQ2;
        lres1.llong = __builtin_e2k_fmad (lres1.llong, lyv2.llong, LKQ4);
        y = lres.value * y / lres1.value;
#else /* __iset__ < 6 */
        y = (KP3 + KP1 * yv2) * y / (KQ4 + yv2 * (KQ2 + yv2));
#endif /* __iset__ < 6 */
    }
    else y = (DB) (1.0F / X);
    yp = (arg1.int0 < 0) ? -PION2 : PION2;
    return (FL) (yp - y);
}

