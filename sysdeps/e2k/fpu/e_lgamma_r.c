/*%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%                                                        %
%     ПРОГРАММА  ВЫЧИСЛЯЕТ  ЗНАЧЕНИЕ  ФУНКЦИИ  'СИНУС'.  %
%  АРГУМЕНТ  В  ДОЛЯХ ПИ,  Т.Е.  SINPI(X) = SIN(X*PI) .  %
%                                                        %
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%*/

static DB sinpi (DB X)
{
    _type_double_bits arg, rez;
    DB y, yv2, t;
    LL ind, k;

    y = arg.value = dfabs (X);
    rez.value = (X - X) * X;
    ind = arg.llong >> 52;
    if (ind >= 0x433)
        return rez.value;       /* |x| >= 2^52 */
    if (ind >= 0x42a) {         /* |x| >= 2^43 */
        k = (arg.llong << (ind - 0x42a)) & 1023;
        t = 0;
    }
    else {
        arg.value += DVAIN52 / 512.0;
        t = y - (arg.value - DVAIN52 / 512.0);
        k = arg.llong;
    }
    ind = k & 511;
    yv2 = t * t;
    if (ind >= 256) {
        ind = 512 - ind;
        t = -t;
    }
    if (ind <= 24) {
        t = (y + DVAIN52) - DVAIN52;
        k = (y == t) ? 0 : k + 24;
        y -= t;
        yv2 = y * y;
        arg.value = y;
        arg.llong &= ~0xffffffffLL;
        t = arg.value * KA1_hi;
        y = (y - arg.value) * PI + arg.value * KA1_lo + KA5 * y * yv2 *
            (KA3 / KA5 + yv2) + y * yv2 * (yv2 * yv2) * (KA9 * yv2 + KA7);
    }
    else {
        y = constsin[ind];
        t = KCO4 * yv2 * (yv2 + KCO2 / KCO4) * y + constsin_lo[ind] +
            constsin[256 - ind] * (KSI5 * t * yv2 * (yv2 + KSI3 / KSI5) + PI * t);
    }
    arg.value = y + t;
    arg.llong += rez.llong + ((k >> 9) << 63);
    return arg.value;
}

